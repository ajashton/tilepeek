inline std::vector<QColor> pickColors(int n)
{
    std::vector<QColor> result;
    if (n <= 0) return result;
    result.reserve(n);
    for (int i = 0; i < n; ++i) {
        int idx = (256 * i) / n;
        auto& c = colors[idx];
        result.emplace_back(c.r, c.g, c.b);
    }
    return result;
}

